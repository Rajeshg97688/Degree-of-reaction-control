function dydt = micro(t,c);
R = 8.314 % gas constant in Pa-m^3/Mol-K
T = 973 % Temperature in Kelvin
Pc1=0.45*101325;
Par=Pc1;
Pc5=0.1*101325;
N=6.023E17; %conversion from molecules in 1 cm^3 to 1 m^3
vi=0.001; %m^3
y1=Pc1*vi/(R*T); %mol P1V0/RT
y5=Pc5*vi/(R*T); %mol P6V0/RT
yar=Par*vi/(R*T); %mol ParV0/RT
ni=y1+y5+yar;
nt=c(1)+c(2)+c(3)+c(4)+c(5)+c(6)+c(7)+c(8)+c(9)+c(10)+c(11)+c(12)+c(13)+c(14)+c(15)+c(16)+yar;
v=nt*vi/ni; %n0RT0/v0 = ntRT0/v same pressure and T0 so v=v0*nt/n0;

kf=[1.1*10^-8 2.2*10^9 1.5*10^11 N*2.2*10^-21 N*3.2*10^-22 N*10^-23 N*9.4*10^-27 N*2.3*10^-23 N*3.1*10^-21 N*6.9*10^-13 N*9.9*10^-24 9.5*10^9 8.7*10^10 6.8*10^3 8.4*10^5 7.1*10^3 5*10^9 5.8*10^8 4.4*10^-1 1.4*10^8 2.4*10^1 4.7*10^6 N*1.9*10^-10 N*2.8*10^-10];

kb=[N*4.1*10^-10 N*1.5*10^-9  N*4.4*10^-9 N*1.9*10^-10 4.1*10^9 N*1.9*10^-12 N*6.6*10^-11 3.9*10^2 4.7*10^4 N*5.7*10^-15 N*3.8*10^-19 N*3.3*10^-9 N*7.3*10^-9 N*3*10^-9  N*2.3*10^-10 N*1.4*10^-9 N*1.5*10^-9 N*3.1*10^-9 N*3.7*10^-12 N*5.1*10^-10 N*5*10^-10 N*3.9*10^-11 1.8*10^-10 2.7*10^-4];


r1 =  kf(1)*(c(1)/v)-kb(1)*(c(2)/v)*(c(3)/v);

r2 = kf(2)*(c(4)/v)-2*kb(2)*(c(5)/v)*(c(5)/v);

r3 = kf(3)*(c(6)/v)-kb(3)*(c(4)/v)*(c(5)/v);

r4 = kf(4)*(c(1)/v)*(c(5)/v)-kb(4)*(c(7)/v)*(c(2)/v);

r5 = kf(5)*(c(5)/v)*(c(1)/v)-kb(5)*(c(8)/v);

r6 = kf(6)*(c(1)/v)*(c(4)/v)-kb(6)*(c(9)/v)*(c(2)/v);

r7 = kf(7)*(c(1)/v)*(c(4)/v)-kb(7)*(c(10)/v)*(c(3)/v);

r8 = kf(8)*(c(1)/v)*(c(4)/v)-kb(8)*(c(11)/v);

r9 = kf(9)*(c(1)/v)*(c(6)/v)-kb(9)*(c(12)/v);

r10 = kf(10)*(c(1)/v)*(c(3)/v)-kb(10)*(c(2)/v)*(c(13)/v);

r11 = kf(11)*(c(1)/v)*(c(2)/v)-kb(11)*(c(14)/v)*(c(3)/v);

r12 = kf(12)*(c(8)/v)-kb(12)*(c(15)/v)*(c(3)/v);

r13 = kf(13)*(c(8)/v)-kb(13)*(c(7)/v)*(c(2)/v);

r14 = kf(14)*(c(7)/v)-kb(14)*(c(3)/v)*(c(5)/v);

r15 = kf(15)*(c(15)/v)-kb(15)*(c(5)/v)*(c(2)/v);

r16 = kf(16)*(c(9)/v)-kb(16)*(c(4)/v)*(c(3)/v);

r17 = kf(17)*(c(9)/v)-kb(17)*(c(5)/v)*(c(7)/v);

r18 = kf(18)*(c(10)/v)-kb(18)*(c(4)/v)*(c(2)/v);

r19 = kf(19)*(c(10)/v)-kb(19)*(c(5)/v)*(c(15)/v);

r20 = kf(20)*(c(16)/v)-kb(20)*(c(4)/v)*(c(7)/v);

r21 = kf(21)*(c(16)/v)-kb(21)*(c(6)/v)*(c(3)/v);

r22 = kf(22)*(c(16)/v)-kb(22)*(c(9)/v)*(c(5)/v);

r23 = kf(23)*(c(3)/v)*(c(3)/v)-kb(23)*(c(13)/v);

r24 = kf(24)*(c(2)/v)*(c(2)/v)-kb(24)*(c(14)/v);


a1 = v*(-r1-r4-r5-r6-r7-r8-r9-r10-r11);

a2 = v*(+r1+r4+r6+r10-r11+r13+r15+r18-2*r24);

a3 = v*(r1+r7-r10-r11-r12+r14+r16+r21-2*r23);

a4 = v*(-r2+r3-r6-r7-r8+r16+r18+r20);   

a5 = v*(2*r2+r3-r4-r5+r14+r15+r17+r19+r22);

a6 = v*(-r3-r9+r21);

a7 = v*(+r4+r13-r14+r17+r20);

a8 = v*(r5-r12-r13);

a9 = v*(r6-r16-r17+r22);   
 
a10 = v*(r7-r18-r19);

a11 = v*(r8);

a12 = v*(r9);

a13 = v*(r10+r23);

a14 = v*(r11+r24);
 
a15 = v*(r12-r15+r19);

a16 = v*(-r20-r21-r22);
%c is concentration of species and t is time
% a1 = d[CH4]/dt, c1=[CH4]
% a2 = d[CH3]/dt, c2=[CH3]
% a3 = d[H]/dt, c3=[H]
% a4 = d[Na2]/dt, c4=[Na2]
% a5 = d[(na)]/dt, c5=[(na)]
% a6 = d[(na)3]/dt, c6=[(na)3]
% a7 = d[NaH]/dt, c7=[NaH]
% a8 = d[HNaCH3]/dt, c8=[HNaCH3]
% a9 = d[Na2H]/dt, c9=[Na2H]
% a10 = d[Na2CH3]/dt, c10=[Na2CH3]
% a11 = d[HNa2CH3]/dt, c11=[HNa2CH3]
% a12 = d[HNa3CH3]/dt, c12=[HNa3CH3]
% a13 = d[H2]/dt, c13=[C2H6]
% a14 = d[C2H6]/dt, c14=[H2]
% a15 = d[(na)CH3]/dt, c15=[(na)CH3]
% a16 = d[Na3H]/dt, c16=[Na3H]

dydt=[a1;a2;a3;a4;a5;a6;a7;a8;a9;a10;a11;a12;a13;a14;a15;a16];
end